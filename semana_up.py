#!/usr/bin/python3
# https://omegaup.com/arena/problem/Semana-UP/

def _main() -> None:
  a = int(input())
  if a == 1:
      print("lunes")
  if a == 2:
      print("martes")
  if a == 3:
      print("miercoles")
  if a == 4:
      print("jueves")
  if a == 5:
      print("viernes")
  if a == 6:
      print("sabado")
  if a == 7:
      print("domingo")

if __name__ == '__main__':
  _main()
